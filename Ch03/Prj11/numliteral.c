#include <stdio.h>

int main(void)
{
	printf("%d", 30);  print("%d", 10);
	printf("%d", 030);  print("%d", 010);
	printf("%d", 0X2F);  print("%d", 0x1b);


	printf("%f", 3.14);  print("%f", 2.0);
	printf("%f", 3.14E+2);  print("%f", 21.8e2);
	printf("%f", 3.14E-2);  print("%f", 218e-3);






	return 0;
}
